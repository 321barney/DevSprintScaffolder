export function useTranslation(locale: Locale = 'fr-MA') {
  const t = (key: keyof typeof translations['fr-MA']): string => {
    return translations[locale][key] || translations['fr-MA'][key] || key;
  };

  return { t, locale };
}

export function formatCurrency(amount: number, locale: Locale = 'fr-MA'): string {
  const currencySymbol = locale === 'ar-MA' ? 'د.م' : 'MAD';
  return `${amount.toLocaleString()} ${currencySymbol}`;
}

export function isRTL(locale: Locale): boolean {
  return locale === 'ar-MA';
}
